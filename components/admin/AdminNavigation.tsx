function MenuSection({ title, icon, items, isExpanded, onToggle, pathname, onClose }: { title: string; icon: React.ReactNode; items: typeof somaAiMenu; isExpanded: boolean; onToggle: () => void; pathname: string; onClose?: () => void }) {
  return (
    <div className="mb-4">
      <button
        onClick={onToggle}
        className="w-full flex items-center justify-between px-4 py-2 text-sm font-semibold text-gray-700 hover:bg-gray-100 rounded-lg transition-colors"
      >
        <span className="flex items-center gap-2 uppercase tracking-wider text-xs text-gray-400">
          {icon}
          {title}
        </span>
        <svg
          className={`w-4 h-4 transition-transform ${isExpanded ? 'rotate-180' : ''}`}
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
        >
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
        </svg>
      </button>

      {isExpanded && (
        <nav className="mt-2 space-y-1">
          {items.map((item) => (
            <Link
              key={item.href}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-4 py-2 ml-4 rounded-lg transition-colors text-sm ${
                pathname === item.href || pathname.startsWith(item.href + '/')
                  ? 'bg-blue-500/10 text-blue-600'
                  : 'text-gray-600 hover:bg-gray-100'
              }`}
            >
              {item.icon}
              <span>{item.label}</span>
            </Link>
          ))}
        </nav>
      )}
    </div>
  );
}

